    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
        int[] selectedRows = cartTable.getSelectedRows();

        for (int i = selectedRows.length - 1; i >= 0; i--) {
            int rowIndex = selectedRows[i];
            Object totalVal = model.getValueAt(rowIndex, model.findColumn("Total"));

            System.out.println("Total value of selected row: " + totalVal);

            try {
                double totalValue = Double.parseDouble(totalVal.toString());
                System.out.println("Parsed value: " + totalValue);

                overallTotal -= totalValue;
                totalLbl.setText("Order Total: " + overallTotal);
                System.out.println("Overall total after deduction: " + overallTotal);

                model.removeRow(rowIndex);
            } catch (NumberFormatException e) {
                System.err.println("Error: Total value is not a valid number.");
            }
        }

    }//GEN-LAST:event_removeBtnActionPerformed

    private void checkoutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutBtnActionPerformed
        if(dineinRadio.isSelected() || takeoutRadio.isSelected())
        {
            if(supercard.isBlank()) 
            {
                JOptionPane.showMessageDialog(null, "Supercard Invalid", "InfoBox: " + "ERROR", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();
            
            if(rowCount < 1) 
            {
                JOptionPane.showMessageDialog(null, "Cart is empty", "InfoBox: " + "ERROR", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            Object[][] tableData = new Object[rowCount][columnCount];

            for (int row = 0; row < rowCount; row++) {
                for (int column = 0; column < columnCount; column++) {
                    tableData[row][column] = model.getValueAt(row, column);
                }
            }
            customerData.tableData = tableData;
            customerData.setTransactionNumber();
            customerData.overallTotal = overallTotal;
            
            String userInput = JOptionPane.showInputDialog(null, "Enter Payment: ");

            if (userInput != null && Integer.parseInt(userInput) >= overallTotal) {
                System.out.println("User input: " + userInput);
            } else if(Integer.parseInt(userInput) < overallTotal)
            {
                JOptionPane.showMessageDialog(null, "Insufficient Money", "InfoBox: " + "ERROR", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            else {
                System.out.println("User cancelled the input dialog.");
                return;
            }
            
            customerData.payment = Integer.parseInt(userInput);
            
            recieptForm rec = new recieptForm();
            rec.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "SELECT TRANSACTION TYPE", "InfoBox: " + "ERROR", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_checkoutBtnActionPerformed

