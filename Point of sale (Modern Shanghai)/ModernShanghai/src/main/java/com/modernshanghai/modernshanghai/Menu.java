    void addRowTable(String item, double price)
    {
        int quantity = 0;
        double total = 0;
        String userInput = JOptionPane.showInputDialog(null, "Enter Quantity");

        if (userInput != null && Integer.parseInt(userInput) > 0) {
            System.out.println("User input: " + userInput);
        } else {
            System.out.println("User cancelled the input dialog.");
            return;
        }
        
        quantity = Integer.parseInt(userInput);
        total = Math.abs(quantity * price);
        String[] row = { item, price + "", quantity + "", total + "" };
        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();

        overallTotal += total;
        totalLbl.setText("Order Total: " + overallTotal);
        model.addRow(row);
    }
